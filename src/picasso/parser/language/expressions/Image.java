package picasso.parser.language.expressions;

/**
* Represents an image in the Picasso language.
* @author Jack d'Entremont
* 
*/
public class Image {
	
	String image;
	
	/**
	 * Creates an image from a string representation of the image file.
	 * 
	 * @param image
	 */
	public Image(String image) {
		this.image = image;
	}
	
}